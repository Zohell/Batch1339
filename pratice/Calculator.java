
public class Calculator {
	public static void main(String[] args) {
		char symbol= '*';
		switch(symbol) {
		case '+':
			System.out.println("The Addition of 5 + 5 is: "+(5+5));
			break;
		case '-':
			System.out.println("The Substraction of 5 - 5 is: "+(5-5));
			break;
		case '*':
			System.out.println("The Multiplication of 5 * 5 is: "+(5*5));
			break;
		case '/':
			System.out.println("The Division of 5 + 5 is: "+(5/5));
			break;
			
		}
		
	     
	}

}
